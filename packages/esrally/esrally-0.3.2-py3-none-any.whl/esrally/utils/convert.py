def bytes_to_kb(b):
    return b / 1024.0


def bytes_to_mb(b):
    return b / 1024.0 / 1024.0


def bytes_to_gb(b):
    return b / 1024.0 / 1024.0 / 1024.0


def seconds_to_ms(s):
    return s * 1000


def ms_to_seconds(ms):
    return ms / 1000.0


def ms_to_minutes(ms):
    return ms / 1000.0 / 60.0
